export function Anuncio(id, titulo, transaccion, descripcion, precio, num_puertas, num_kms, potencia) {
    this.id = id;
    this.titulo = titulo;
    this.transaccion = transaccion;
    this.descripcion = descripcion;
    this.precio = precio;
    this.num_puertas = num_puertas;
    this.num_kms = num_kms;
    this.potencia = potencia;
}
